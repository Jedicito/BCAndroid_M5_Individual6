package chl.ancud.m5_individual6_2;

public class Usuario {
    private String nombre;
    private String imagen;

    public Usuario(String nombre, String imagen) {
        this.nombre = nombre;
        this.imagen = imagen;
    }

    public String getNombre() {
        return nombre;
    }

    public String getImagen() {
        return imagen;
    }


}
